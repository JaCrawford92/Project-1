let currentQuestionIndex = 0;
let currentQuestion = null;
let correctAnswers = 0;
let currentQuestionArray;


// START GAME FUNCTION //
function startGame(difficulty) {
    currentQuestionIndex = 0;
    correctAnswers = 0;

    document.getElementById("startBtn").style.display = "none";
    document.getElementById("startOverBtn").style.display = "none";
    document.getElementById("question").style.display = "block"
    document.getElementById("options").style.display = "block"
    document.getElementById("nextBtn").style.display = "block"

    if (difficulty === "easy"){
        currentQuestionArray = easyQuestions;
    } else if (difficulty === "medium") {
        currentQuestionArray = mediumQuestions;
    } else if (difficulty === "hard") {
        currentQuestionArray = hardQuestions;
    } else if (difficulty === "expert") {
        currentQuestionArray = expertQuestions;
    }
    loadQuestion();
    console.log("Game started!");
}

// EVENT LISTENER TO MAKE SURE THE DIFFICULTY BUTTONS APPEAR AFTER CLICKING START GAME //
document.getElementById("startBtn").addEventListener("click", function() {
    document.getElementById("easyBtn").style.display = 'block';
    document.getElementById("mediumBtn").style.display = 'block';
    document.getElementById("hardBtn").style.display = 'block';
    document.getElementById("expertBtn").style.display = 'block';

    document.getElementById('startBtn').style.display = 'none'
});

// EVENT LISTENER FOR DIFFICULTY //
const easyButton = document.getElementById('easyBtn');
const mediumButton = document.getElementById('mediumBtn');
const hardButton = document.getElementById('hardBtn');
const expertButton = document.getElementById('expertBtn');
    
easyButton.addEventListener("click", function() {
    startGame('easy');
    hideDiffBtn();
});
mediumButton.addEventListener("click", function(){
    startGame('medium');
    hideDiffBtn();
});
hardButton.addEventListener("click", function(){
    startGame('hard');
    hideDiffBtn();
});
expertButton.addEventListener("click", function(){
    startGame('expert');
    hideDiffBtn();
});

function hideDiffBtn(){
    easyButton.style.display = "none";
    mediumButton.style.display = "none";
    hardButton.style.display = "none";
    expertButton.style.display = "none";
}

document.getElementById("nextBtn").addEventListener("click", nextQuestion);


// QUESTION LOADING FUNCTION //
function loadQuestion(questions) {
    if(currentQuestionIndex >= currentQuestionArray.length){
        showRanking();
        return;
    }
    currentQuestion = currentQuestionArray[currentQuestionIndex];
    const questionElement = document.getElementById("question");
    const optionsElement = document.getElementById("options");
    
    questionElement.textContent = currentQuestion.question;
    optionsElement.innerHTML = "";

    currentQuestion.options.forEach((option, index) => {
        const button = document.createElement("button");
        button.textContent = option;
        button.classList.add("answer-button");
        button.onclick = () => selectAnswer(index);
        optionsElement.appendChild(button);
    });

}

// ANSWER SELECTION FUNTION //
function selectAnswer(optionIndex) {
    const selectedOption = currentQuestion.options[optionIndex];
    const correctAnswer = currentQuestion.answer;
    const feedbackElement = document.getElementById('feedback');
    
    // THE CONDITIONAL TO LET YOU KNOW IF YOUR ANSWER IS CORRECT OR NOT //
    if (selectedOption === correctAnswer) {
        correctAnswers++;
        feedbackElement.textContent = 'Correct! Well done mortal!';
        feedbackElement.style.color = 'white';
        // alert("Correct! Well done mortal!");
    } else {
        feedbackElement.textContent = `Incorrect! The correct answer is ${correctAnswer}`;
        feedbackElement.style.color = 'red';
        // alert(`Wrong mortal! The correct answer is: ${correctAnswer}`);
    }

    const button = document.querySelectorAll("#options button");
    button.forEach(button => {
        button.disabled = true;
    });
    
    setTimeout(() => {
        feedbackElement.textContent = '';
        nextQuestion();
        button.forEach(button => {
            button.disabled = false;
        });
    }, 2000);
    // nextQuestion();
}

// NEXT QUESTION FUNCTIONS //
function nextQuestion() {
    currentQuestionIndex++; 
    loadQuestion();
}

// RANKINGS AFTER ANSWERING THE QUESTIONS FUNCTIONS //
function showRanking(){
    document.getElementById("question").style.display = "none";
    document.getElementById("options").style.display = "none";
    document.getElementById("nextBtn").style.display = "none";
    const rankingElement = document.getElementById("ranking");

    // THIS LETS YOU KNOW IF HOW WILL YOU DID ON THE TRIVIA //
    if (correctAnswers === currentQuestionArray.length) {
        rankingElement.textContent = "Perfect! You're a Greek God";
    } else if (correctAnswers >= currentQuestionArray.length / 2) {
        rankingElement.textContent = "Good Job! You're a Demi-God";
    } else {
        rankingElement.textContent = "Keeping trying mortal";
    }
    rankingElement.style.display = "block";
    document.getElementById("startOverBtn").style.display = "block";
}

document.getElementById("startOverBtn").addEventListener("click", startOver);

// START OVER FUNCTION //
function startOver() {
    currentQuestionIndex = 0;
    currentQuestion = null;
    correctAnswers = 0;
    currentQuestionArray = null;

    document.getElementById("ranking").style.display = "none"
    document.getElementById("startOverBtn").style.display = "none"
    document.getElementById("question").style.display = "none"
    document.getElementById("options").style.display = "none"
    document.getElementById("nextBtn").style.display = "none"

    easyButton.style.display = "block";
    mediumButton.style.display = "block";
    hardButton.style.display = "block";
    expertButton.style.display = "block";
    // loadQuestion();
}


